package com.example.advancedrecyclerviews.recyclercard;

public class Stones {
    private String nameStone;
    private int imgStone;

    public Stones(String nameStone, int imgStone) {
        this.nameStone = nameStone;
        this.imgStone = imgStone;
    }

    public String getNameStone() {
        return nameStone;
    }

    public void setNameStone(String nameStone) {
        this.nameStone = nameStone;
    }

    public int getImgStone() {
        return imgStone;
    }

    public void setImgStone(int imgStone) {
        this.imgStone = imgStone;
    }
}
